package Model;

public class ReferencePoint {
    private double x;
    private double y;

    public ReferencePoint() {
        this.x = 0;
        this.y = 0;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "R("+ x + ", " + y + ")";
    }
}
